# =========================
# compute weight DTS
# =========================
def weight_from_ddt():

    w = []

    for i in range(SIZE):

        if i == 0:
            w.append(0)
            continue

        m = max(DDT[i])

        if m == 0:
            w.append(0)
        else:
            w.append(int(math.ceil(-math.log2(m/32))))

    return w


# =========================
# compute weight LTS
# =========================
def weight_from_lat():

    w = []

    for i in range(SIZE):

        if i == 0:
            w.append(0)
            continue

        m = max(abs(x) for x in LAT[i])

        if m == 0:
            w.append(0)
        else:
            w.append(int(math.ceil(-math.log2(m/32))))

    return w


# =========================
# reverse weight
# =========================
def reverse_weight(table, weight):

    rev = []

    for col in range(SIZE):

        vals = []

        for row in range(SIZE):

            if table[row][col] != 0:
                vals.append(weight[row])

        rev.append(min(vals) if vals else 0)

    return rev


# =========================
# print C++
# =========================
def print_cpp(name, arr):

    print(f"unsigned int {name}[32] = {{")

    for i in range(0,32,8):

        row = arr[i:i+8]

        print("   " + ", ".join(str(x) for x in row) + ",")

    print("};\n")


# =========================
# find patterns
# =========================
def patterns_weight_gt(weight, t):

    return [i for i in range(32) if weight[i] > t]


# =========================
# pattern -> boolean term
# =========================
def pattern_term(p):

    terms = []

    for b in reversed(range(5)):

        if (p >> b) & 1:
            terms.append(f"stateAtB.getRow({b})")
        else:
            terms.append(f"~stateAtB.getRow({b})")

    return "(" + " & ".join(terms) + ")"


# =========================
# boolean expression
# =========================
def boolean_function(patterns):

    if not patterns:
        return "0"

    terms = [pattern_term(p) for p in patterns]

    return " |\n           ".join(terms)


