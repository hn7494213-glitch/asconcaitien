# =========================
# weight
# =========================
def weight_ddt(count):
    return int(math.log2(32 / count))

def weight_lat(val):
    return int(math.log2(32 / abs(val)))


# =========================
# affine space computation
# =========================
def compute_affine(outputs):

    if len(outputs) == 0:
        return (0, [])

    offset = outputs[0]

    vecs = [x ^ offset for x in outputs]

    basis = []

    for v in vecs:

        w = v

        for b in basis:
            w = min(w, w ^ b)

        if w != 0:
            basis.append(w)

    return offset, basis


# =========================
# compute tables
# =========================
def compute_tables(table, weight_func):

    weightListPerInput = []
    directOutputListPerInput = []
    reverseOutputListPerInput = []
    weightReverseOutputListPerInput = []
    minRevWeightListPerOutput = [999] * SIZE
    affinePerInput = []

    for a in range(SIZE):

        outputs = []
        weights = []

        minw = 999

        for b in range(SIZE):

            c = table[a][b]

            if c != 0:

                w = weight_func(c)

                outputs.append(b)
                weights.append(w)

                if w < minw:
                    minw = w

                if w < minRevWeightListPerOutput[b]:
                    minRevWeightListPerOutput[b] = w

        if minw == 999:
            minw = 0

        weightListPerInput.append(minw)

        pairs = list(zip(outputs, weights))
        pairs.sort(key=lambda x: x[1])

        reverseOutputListPerInput.append([p[0] for p in pairs])
        weightReverseOutputListPerInput.append([p[1] for p in pairs])
        directOutputListPerInput.append(outputs)

        offset, basis = compute_affine(outputs)
        affinePerInput.append((offset, basis))

    return (
        weightListPerInput,
        directOutputListPerInput,
        reverseOutputListPerInput,
        affinePerInput,
        weightReverseOutputListPerInput,
        minRevWeightListPerOutput,
    )


# =========================
# C++ formatting helpers
# =========================
def to_hex_list(lst):
    return [hex(x) for x in lst]

def print_cpp_affine(name, affine_list):

    print(name + " = {")

    for i,(offset,basis) in enumerate(affine_list):

        basis_hex = ", ".join(hex(x) for x in basis)

        print(f"    AffineSpaceOfColumns({hex(offset)}, {{{basis_hex}}})", end="")

        if i != len(affine_list)-1:
            print(",")

    print("};\n")


# =========================
# print helper
# =========================
def print_cpp_table(name, table, hex_values=False):

    print(name + " = {")

    # nếu là list số nguyên
    if isinstance(table[0], int):

        line = ", ".join(str(x) for x in table)
        print("    " + line)

    else:

        for i,row in enumerate(table):

            if hex_values:
                row = [hex(x) for x in row]

            line = ", ".join(str(x) for x in row)

            print("    {" + line + "}", end="")

            if i != len(table)-1:
                print(",")

    print("\n};\n")


# =========================
# main
# =========================
def main():

    tables_ddt = compute_tables(DDT, weight_ddt)
    tables_lat = compute_tables(LAT, weight_lat)

    names = [
        "weightListPerInput",
        "directOutputListPerInput",
        "reverseOutputListPerInput",
        "affinePerInput",
        "weightReverseOutputListPerInput",
        "minRevWeightListPerOutput",
    ]

    print("===== FROM DDT (DTS) =====\n")

    for name,table in zip(names,tables_ddt):

        if name == "affinePerInput":
            print_cpp_affine(name+"_DDT",table)

        elif name == "directOutputListPerInput" or name == "reverseOutputListPerInput":
            print_cpp_table(name+"_DDT",table,True)

        else:
            print_cpp_table(name+"_DDT",table)


    print("\n===== FROM LAT (LTS) =====\n")

    for name,table in zip(names,tables_lat):

        if name == "affinePerInput":
            print_cpp_affine(name+"_LAT",table)

        elif name == "directOutputListPerInput" or name == "reverseOutputListPerInput":
            print_cpp_table(name+"_LAT",table,True)

        else:
            print_cpp_table(name+"_LAT",table)


if __name__ == "__main__":
    main()